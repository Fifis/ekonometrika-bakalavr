set.seed(199)
x <- rchisq(1000, 30)
x <- (x-mean(x))/sd(x)
levels <- seq(0.01, 0.99, 0.01)
xq <- quantile(x, levels)
tq <- qt(levels, 5)
f <- rf(300, 40, 100)
fq <- quantile(f, levels)
png("r-quantiles-1.png", 500, 500, pointsize=18)
par(mar=c(5,4,1,1))
qqplot(xq, tq)
qqline(tq, lwd=2, lty=2)
dev.off()
png("r-quantiles-2.png", 500, 500, pointsize=18)
par(mar=c(5,4,1,1))
qqplot(xq, fq)
qqline(fq, lwd=2, lty=2)
dev.off()